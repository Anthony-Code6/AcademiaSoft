AS
BEGIN
	IF EXISTS (SELECT * FROM PERIODO WHERE ID=@ID)
		BEGIN
			UPDATE PERIODO SET fectrimestre1=@TRIMESTRE1,fectrimestre2=@TRIMESTRE2,fectrimestre3=@TRIMESTRE3,fectrimestre4=@TRIMESTRE4 WHERE ID=@ID;
		END
