GO

CREATE PROCEDURE SP_ELIMINAR_PERIODO
@CODIGO INT
AS
BEGIN
	DECLARE @MENSAJE VARCHAR(100);
	IF EXISTS (select * from periodo as p inner join matricula as m
					on m.idperiodo=p.id where p.id=@CODIGO)
		BEGIN
			SET @MENSAJE = 'Error el Periodo no se puede Eliminar, esta asigando a una Matricula';		
		END
	ELSE
		BEGIN 
			DELETE FROM PERIODO WHERE ID=@CODIGO;
			SET @MENSAJE = 'Se Elimino Correctamente';	
		END

	select @MENSAJE as Error;
END
GO

